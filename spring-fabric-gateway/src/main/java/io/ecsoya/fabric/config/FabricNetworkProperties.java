package io.ecsoya.fabric.config;

public class FabricNetworkProperties {

	private String name;

	private String file;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getFile() {
		return file;
	}

	public void setFile(String file) {
		this.file = file;
	}
}
